package priv.lint.Entity;

public class Unscramble {
    int unscrambleId;
    int unscramblePolicyId;
    String unscrambleType;
    String unscrambleContent;

    public int getUnscrambleId() {
        return unscrambleId;
    }

    public void setUnscrambleId(int unscrambleId) {
        this.unscrambleId = unscrambleId;
    }

    public int getUnscramblePolicyId() {
        return unscramblePolicyId;
    }

    public void setUnscramblePolicyId(int unscramblePolicyId) {
        this.unscramblePolicyId = unscramblePolicyId;
    }

    public String getUnscrambleType() {
        return unscrambleType;
    }

    public void setUnscrambleType(String unscrambleType) {
        this.unscrambleType = unscrambleType;
    }

    public String getUnscrambleContent() {
        return unscrambleContent;
    }

    public void setUnscrambleContent(String unscrambleContent) {
        this.unscrambleContent = unscrambleContent;
    }

}
